"use strict";

// We can pass values into a function with arguments. You can use a return statement to send a value back out of a function.

// Task: Create a function timesFive that accepts one argument, multiplies it by 5, and returns the new value.

function timesFive(arg) {
    return 5 * arg;
}

timesFive(5);
console.log("Result: ", timesFive(5));
